import 'package:flutter/material.dart';

void main() {
  runApp(MyApp());
}

class MyApp extends StatelessWidget {
  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Flutter Demo',
      theme: ThemeData(
        primarySwatch: Colors.blue,
        visualDensity: VisualDensity.adaptivePlatformDensity,
      ),
      home: MyHomePage(),
    );
  }
}

class MyHomePage extends StatefulWidget {
  MyHomePage({Key key}) : super(key: key);

  @override
  _MyHomePageState createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  List<String> taskTitles = [
    'Understand Code',
    'Figure out duplication',
    'Refactor',
    'Add comments',
    'commit code',
    'push to github',
  ];

  List<bool> taskStates = [
    true,
    false,
    true,
    false,
    false,
    false,
  ];

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        backgroundColor: Colors.teal,
        title: Text('Your Tasks'),
      ),
      body: Container(
        color: Colors.teal,
                // REFACTOR INTO ListView.builder
        child: ListView(children: <Widget>[
          // REFACTOR OUT INTO STATEFUL TASK WIDGET
          Card(
            child: CheckboxListTile(
              value: taskStates[0],
              onChanged: (newValue) {
                setState(() {
                  taskStates[0] = newValue;
                });
              },
              activeColor: Colors.teal,
              title: Text(taskTitles[0], style: TextStyle(fontSize: 20)),
              controlAffinity: ListTileControlAffinity.leading,
            ),
          ),
          Card(
            child: CheckboxListTile(
              value: taskStates[1],
              onChanged: (newValue) {
                setState(() {
                  taskStates[1] = newValue;
                });
              },
              activeColor: Colors.teal,
              title: Text(taskTitles[1], style: TextStyle(fontSize: 20)),
              controlAffinity: ListTileControlAffinity.leading,
            ),
          ),
          Card(
            child: CheckboxListTile(
              value: taskStates[2],
              onChanged: (newValue) {
                setState(() {
                  taskStates[2] = newValue;
                });
              },
              activeColor: Colors.teal,
              title: Text(taskTitles[2], style: TextStyle(fontSize: 20)),
              controlAffinity: ListTileControlAffinity.leading,
            ),
          ),
        ]),
      ),
    );
  }
}
